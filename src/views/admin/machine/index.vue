      items: [],
    };
  },

  watch: {
    currentPage: {
      handler: function () {
        this.fetchData();
      },
    },
    getmachineList: {
      handler: function () {
        this.items = this.getmachineList;
      },
    },
    search: {
      handler: function () {
        this.items = this.getmachineList.filter((x) => {
          return x.machine_name.includes(this.search);
        });
      },
    },
  },
  methods: {
    hasPermission(obj) {
      // if (
      //   this.userDetails &&
      //   this.userDetails.user &&
      //   this.userDetails.user.user_role == "super_admin"
      // ) {
      return true;
